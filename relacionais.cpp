#include <iostream>
using namespace std;

int main() {
       //Declaração da variavel x , seu tipo e valor    	
       int x = 10;
       //Declaração da variavel y , seu tipo e valor
       int y = 20;
       
       //Condição se x maior que y 
       if (x > y){
	   //Tela mostrando a mensagem digitada para o usuario se a condição x maior que y for cumprida
           cout<<"x é maior que y"<<endl;
       }
       //Condição se x menor que y 
       if (x < y){
	   //Tela mostrando a mensagem digitada para o usuario se a condição x menor que y for cumprida
           cout<<"x é menor que y"<<endl;
       }
       //Condição se x maior igual a y 
       if (x >= y){
	   //Tela mostrando a mensagem digitada para o usuario se a condição x maior ou igual a y for cumprida
           cout<<"x é maior ou igual a y"<<endl;
       }
       //Condição se x menor igual a y 
       if (x <= y){ 
	   //Tela mostrando a mensagem digitada para o usuario se a condição x menor ou igual a y for cumprida
           cout<<"x é menor ou igual a y"<<endl;
       }
       //Condição se x igual a y 
       if (x == y){
	   //Tela mostrando a mensagem digitada para o usuario se a condição x é igual a y for cumprida
           cout<<"x é igual a y"<<endl;
       }
       //Condição se x diferente de y 
       if (x != y){
	   //Tela mostrando a mensagem digitada para o usuario se a condição x diferente de y for cumprida
           cout<<"x é diferente de y"<<endl;
       }
    
    

    return 0;
}