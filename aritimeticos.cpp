#include <iostream>
using namespace std;

int main() {
    	
	//Declarando variaveis e seus devidos valores
        int num1 = 10 , num2 = 4;
	//Declarando algumas variaveis
        int soma, multi,  divi,  modulo, subt;
       

        //Formula da variavel "soma" que foi declarada
        soma = num1 + num2;
	//Formula da variavel "multi" que foi declarada
        multi = num1 * num2;
	//Formula da variavel "divi" que foi declarada
        divi = num1 / num2;
	//Formula da variavel "modulo" que foi declarada
        modulo = num1%num2;
	//Formula da variavel "subt" que foi declarada
        subt = num1 - num2;
        
	//Tela mostrando o valor da variavel "soma" após cumprir a formula da mesma
        cout<<soma<<endl;
	//Tela mostrando o valor da variavel "multi" após cumprir a formula da mesma
        cout<<multi<<endl;
	//Tela mostrando o valor da variavel "divi" após cumprir a formula da mesma
        cout<<divi<<endl;
	//Tela mostrando o valor da variavel "modulo" após cumprir a formula da mesma
        cout<<modulo<<endl;
	//Tela mostrando o valor da variavel "subt" após cumprir a formula da mesma
        cout<<subt<<endl;
        
    

    return 0;
}